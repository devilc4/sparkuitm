						Besides, the redundancy of student’s name in the list because of manually process and staff notify the student regarding their's parcel through a piece of paper on college notice board. 
						Thus, Student’s Parcel Tracking and Management System is proposed to manage the parcel management between staff and student. 
						The system capable for the staff and student to add the information about the parcel and for the student, they can add the remark regarding their parcel. Via this system, 
						the student will receive the notification regarding their parcel if arrived. The notification deliver is through the system and SMS.
						The methodology consists of five phases which are planning process, analysis process, design process, development process, and testing and evaluation process. 
